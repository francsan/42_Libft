#include "libft.h"

void	*ft_calloc(size_t count, size_t size)
{
	size_t	total_size;
	void	*dst;

	total_size = size * count;
	dst = malloc(total_size);
	if (dst == NULL)
		return (0);
	ft_memset(dst, 0, total_size);
	return (dst);
}
