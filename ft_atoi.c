#include "libft.h"

int	ft_atoi(const char *str)
{
	int	i;
	int	j;

	i = 1;
	j = 0;
	while ((*str >= 9 && *str <= 13) || *str == 32)
		str++;
	if (*str == '+' || *str == '-')
	{
		if (*str == '-')
			i = -1;
		str++;
	}
	while (*str >= '0' && *str <= '9')
	{
		j = (*str - 48) + (j * 10);
		str++;
		if (i * j > 2147483647)
			return (-1);
		if (i * j < -2147483648)
			return (0);
	}
	return (i * j);
}
